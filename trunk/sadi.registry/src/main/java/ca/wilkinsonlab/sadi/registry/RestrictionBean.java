package ca.wilkinsonlab.sadi.registry;

/**
 * 
 * @author Luke McCarthy
 */
public class RestrictionBean
{
	private String onProperty;
	private String valuesFrom;
	
	/**
	 * @return the onProperty
	 */
	public String getOnProperty()
	{
		return onProperty;
	}
	/**
	 * @param onProperty the onProperty to set
	 */
	public void setOnProperty(String onProperty)
	{
		this.onProperty = onProperty;
	}
	/**
	 * @return the valuesFrom
	 */
	public String getValuesFrom()
	{
		return valuesFrom;
	}
	/**
	 * @param valuesFrom the valuesFrom to set
	 */
	public void setValuesFrom(String valuesFrom)
	{
		this.valuesFrom = valuesFrom;
	}
}
